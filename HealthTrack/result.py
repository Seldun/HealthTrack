import json


def health_advice(weight, height, complaints):
    advice = []

    # Общие советы
    advice.append("Старайтесь поддерживать сбалансированное питание, включая разнообразные продукты.")
    advice.append(
        "Регулярная физическая активность важна для поддержания здоровья. Попробуйте заниматься не менее 150 минут в неделю.")
    advice.append(
        "Следите за своим психоэмоциональным состоянием. Практикуйте медитацию или йогу для снижения стресса.")

    # Условия для чрезмерного повышения показателей
    if weight > 100:  # Например, если вес больше 100 кг
        advice.append(
            "Ваш вес значительно превышает норму. Рекомендуется обратиться к врачу или диетологу для составления индивидуального плана питания и тренировок.")

    if height > 2.0 and weight > 120:  # Высокий рост и избыточный вес
        advice.append(
            "При таком росте и весе важно следить за здоровьем. Пожалуйста, проконсультируйтесь с медицинским специалистом.")

    # Ответы на распространённые жалобы
    for complaint in complaints:
        if complaint == "усталость":
            advice.append(
                "Чувство усталости может быть связано с недостатком сна или стрессом. Попробуйте улучшить режим сна и включить в распорядок дня время для отдыха.")
        elif complaint == "боли в спине":
            advice.append(
                "Боли в спине могут быть вызваны неправильной осанкой или малоподвижным образом жизни. Рассмотрите возможность занятий физической активностью, направленной на укрепление мышц спины.")
        elif complaint == "головные боли":
            advice.append(
                "Головные боли могут быть вызваны стрессом или обезвоживанием. Убедитесь, что вы пьете достаточно воды и находите время для отдыха.")
        elif complaint == "проблемы со сном":
            advice.append(
                "Проблемы со сном могут быть связаны с высоким уровнем стресса или неправильным режимом. Попробуйте установить регулярное время для сна и избегайте экранов перед сном.")
        elif complaint == "повышенное давление":
            advice.append(
                "Повышенное давление может быть связано с образом жизни. Рекомендуется снизить потребление соли, увеличить физическую активность и проконсультироваться с врачом.")

    return advice


# Чтение данных из файла data.json
with open('data.json', 'r', encoding='utf-8') as file:
    data = json.load(file)

# Вызов функции с аргументами из файла
results = health_advice(data["window2"]['weight'], data["window2"]['height'], data["window3"]['symptoms'])

# Запись результатов в файл answer.txt
with open('answer.txt', 'w', encoding='utf-8') as file:
    for item in results:
        file.write(item + '\n')
